import math

def coprime(a, b):
    return math.gcd(a,b) == 1
    
def sol(a,b):

    d1,d2 = {},{}
    for i in a:
        d1[i]=True
    for i in b:
        d2[i]=True
    count = 0
    for i_key in d1.keys():
        for j_key in d2.keys():
            if d1[i_key] == True and d2[j_key] == True:
                if not coprime(i_key,j_key):
                    count+=1
                    d1[i_key] = False
                    d2[j_key] = False
    return count
       
    

